//Logicas Booleanas
void main() {
  print("-----Operadores logicos( &&(e) ||(ou) ------)\n");

  var verdadeiro = false;
  print("inferencia do tipo boleano : $verdadeiro \n");

  // ao usar ! significa que vc está invertendo o valor logico
  bool falso = !false;
  print(
      "ao usar ! significa que vc está invertendo o valor logico : $falso \n");

  print(
      "Quando utilizar o operador ||(ou) precisa que uma condição precisa ser verdadeira para retornar verdadeiro \n");
  print("Qaundo utilizar o operador &&(e) as duas condições precisam ser verdadeiras para retornar verdadeiro\n");
}
